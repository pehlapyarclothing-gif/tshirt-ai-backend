/**
 * upgraded-server.js - T-Shirt Customizer Backend (Corrected AI Payload)
 */

import express from "express";
import cors from "cors";
import fetch from "node-fetch";

const app = express();
const PORT = process.env.PORT || 3000;

// Replicate API Configuration
const REPLICATE_API_KEY = process.env.REPLICATE_API_KEY; 
const REPLICATE_ENDPOINT = "https://api.replicate.com/v1/predictions";
const INSTANT_ID_MODEL = "2e4785a4d80dadf580077b2244c8d7c05d8e3faac04a04c02d8e099dd2876789"; 

app.use(cors());
app.use(express.json());

app.post("/api/tshirt-preview", async (req, res) => {
  const { customerImageUrl, maskUrl, referenceStyleUrl } = req.body;

  if (!customerImageUrl || !maskUrl) {
    return res.status(400).json({ error: "Missing required customerImageUrl or maskUrl." });
  }

  // Uses the design sent from Shopify, or defaults to your template
  const inputPromptImage = referenceStyleUrl || "https://res.cloudinary.com/dugxzgkvy/image/upload/v1783858281/1000113069_l18mfk.png";

  try {
    console.log(`Starting generation for user image: ${customerImageUrl}`);

    const startResponse = await fetch(REPLICATE_ENDPOINT, {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${REPLICATE_API_KEY}`,
        "Content-Type": "application/json"
      },
      body: JSON.stringify({
        version: INSTANT_ID_MODEL,
        input: {
          image: customerImageUrl, 
          prompt: "A male model wearing an oversized streetwear t-shirt, casual pose, highly detailed, integrated perfectly.",
          negative_prompt: "woman, female, dress, skirt, hallucination, text, distorted face, different ethnicity, artifacts",
          mask: maskUrl,                     // RESTORED: Forces the AI inside the t-shirt silhouette
          structure_image: inputPromptImage, // RESTORED: Forces the AI to use your typography design
          image_prompt_method: "ip-adapter",
          face_detailer: true,
          controlnet_weights: "0.8",
          disable_safety_checker: true
        }
      })
    });

    if (!startResponse.ok) {
      const errorText = await startResponse.text();
      console.error("Replicate API Rejected the Request:", errorText);
      throw new Error(`Replicate API Error: ${errorText}`);
    }

    let replicateData = await startResponse.json();

    const checkStatusUrl = replicateData.urls.get;
    let status = replicateData.status;

    console.log("AI is processing. Waiting for completion...");

    while (status !== "succeeded" && status !== "failed") {
      await new Promise(resolve => setTimeout(resolve, 2000));
      const pollResponse = await fetch(checkStatusUrl, {
        headers: {
          "Authorization": `Bearer ${REPLICATE_API_KEY}`,
          "Content-Type": "application/json"
        }
      });
      replicateData = await pollResponse.json();
      status = replicateData.status;
    }

    if (status === "failed") throw new Error("Replicate AI generation failed during processing.");
    if (!replicateData.output || !replicateData.output[0]) throw new Error("No image output generated by the AI.");

    const outputUrl = replicateData.output[0];
    res.json({ aiImageUrl: outputUrl });

  } catch (error) {
    console.error("AI Generation Error:", error.message);
    res.status(500).json({ error: error.message || "An unexpected error occurred." });
  }
});

app.listen(PORT, () => {
  console.log(`Upgraded Customizer Server running on port ${PORT}`);
});
