/**
 * upgraded-server.js - T-Shirt Customizer Backend (True Face Swap Fix)
 */

import express from "express";
import cors from "cors";
import fetch from "node-fetch";

const app = express();
const PORT = process.env.PORT || 3000;

// Replicate API Configuration
const REPLICATE_API_KEY = process.env.REPLICATE_API_KEY; 
const REPLICATE_ENDPOINT = "https://api.replicate.com/v1/predictions";

// We are switching from InstantID to a dedicated Face Swap model
const FACE_SWAP_MODEL = "278a81e7ebb22db98bcba54de985d22cc1abeead2754eb1f2af717247be69b34"; 

app.use(cors());
app.use(express.json());

app.post("/api/tshirt-preview", async (req, res) => {
  const { customerImageUrl, referenceStyleUrl } = req.body; 

  if (!customerImageUrl) {
    return res.status(400).json({ error: "Missing customerImageUrl." });
  }

  // The base design we want to keep 100% identical (VEER, ADYA, etc.)
  const targetDesignImage = referenceStyleUrl || "https://res.cloudinary.com/dugxzgkvy/image/upload/v1783858281/1000113069_l18mfk.png";

  try {
    console.log(`Starting TRUE face swap for user image: ${customerImageUrl}`);

    const startResponse = await fetch(REPLICATE_ENDPOINT, {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${REPLICATE_API_KEY}`,
        "Content-Type": "application/json"
      },
      body: JSON.stringify({
        version: FACE_SWAP_MODEL,
        input: {
          input_image: targetDesignImage, // The original t-shirt graphic (target)
          swap_image: customerImageUrl    // The customer's face (source)
        }
      })
    });

    if (!startResponse.ok) {
      const errorText = await startResponse.text();
      console.error("Replicate API Rejected the Request:", errorText);
      throw new Error(`Replicate API Error: ${errorText}`);
    }

    let replicateData = await startResponse.json();

    const checkStatusUrl = replicateData.urls.get;
    let status = replicateData.status;

    console.log("AI is processing face swap. Waiting for completion...");

    while (status !== "succeeded" && status !== "failed") {
      await new Promise(resolve => setTimeout(resolve, 2000));
      const pollResponse = await fetch(checkStatusUrl, {
        headers: {
          "Authorization": `Bearer ${REPLICATE_API_KEY}`,
          "Content-Type": "application/json"
        }
      });
      replicateData = await pollResponse.json();
      status = replicateData.status;
    }

    if (status === "failed") throw new Error("Face swap failed during processing.");
    if (!replicateData.output) throw new Error("No image output generated by the AI.");

    // Note: This specific face swap model returns a single string URL directly, not an array!
    const outputUrl = replicateData.output;
    console.log(`Generation complete. AI Image URL: ${outputUrl}`);
    
    res.json({ aiImageUrl: outputUrl });

  } catch (error) {
    console.error("AI Generation Error:", error.message);
    res.status(500).json({ error: error.message || "An unexpected error occurred." });
  }
});

app.listen(PORT, () => {
  console.log(`True Face Swap Server running on port ${PORT}`);
});
